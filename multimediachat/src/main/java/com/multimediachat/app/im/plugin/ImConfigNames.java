package com.multimediachat.app.im.plugin;

/** Defines the configuration names for the IM engine. */
public interface ImConfigNames {
    /** The name of the protocol. */
    String PROTOCOL_NAME = "im.protocol";

    /** The default domain. */
    String DEFAULT_DOMAIN = "im.default-domain";

    /** The path of the plugin. */
    String PLUGIN_PATH = "im.plugin.path";

    /** The class name of the plugin implementation. */
    String PLUGIN_CLASS = "im.plugin.class";

    /** The version of the plugin. */
    String PLUGIN_VERSION = "im.plugin.version";
}
